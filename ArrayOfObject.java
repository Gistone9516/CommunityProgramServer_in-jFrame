package CServer;

public class ArrayOfObject {
	String department;
	int year;
	String name;
	String sortation;
	String classnum;
	
	ArrayOfObject(String depart, int yea, String nam, String sort, String num){
		this.department = depart;
		this.year = yea;
		this.name = nam;
		this.sortation = sort;
		this.classnum = num;
	}
	
}
